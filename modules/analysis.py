def _check_expiry(results):
    """
    Helper for DNS-001 rule.
    Checks if domain expires within 90 days.
    """
    from datetime import datetime

    expiry = results.get("whois", {}).get("expires")
    if not expiry:
        return False

    try:
        exp_date = datetime.strptime(expiry, "%Y-%m-%d")
        days_left = (exp_date - datetime.now()).days
        return 0 < days_left < 90
    except Exception:
        return False


# ══════════════════════════════════════════════════════
#  SECTION 1 — WAF DETECTION
# ══════════════════════════════════════════════════════

def run_waf_detection(base_url, config):
    """
    Sends requests to the target and checks response
    headers + body for WAF fingerprints.

    Also sends a deliberately malicious payload to trigger
    WAF block page — WAFs often reveal themselves when blocking.

    Returns:
    - detected WAFs (list)
    - bypass headers to use in subsequent requests
    """

    detected = []

    # ── normal request fingerprinting ─────────────────────────────────────
    try:
        r = requests.get(
            base_url,
            timeout=config.get("timeout", 8),
            verify=False,
            headers={"User-Agent": "Mozilla/5.0 Chrome/122.0.0.0 Safari/537.36"}
        )

        # build a single string to search through
        # includes all headers + first 2000 chars of body
        headers_str = " ".join([
            f"{k.lower()}: {v.lower()}"
            for k, v in r.headers.items()
        ])
        body_sample = r.text[:2000].lower()
        search_text = headers_str + " " + body_sample

        for waf_name, sigs in WAF_SIGNATURES.items():
            # check header signatures
            header_hit = any(
                sig in headers_str
                for sig in sigs["headers"]
            )

            # check body signatures
            body_hit = any(
                sig in body_sample
                for sig in sigs["body"]
            )

            # check server header specifically
            server_val = r.headers.get("Server", "").lower()
            server_hit = any(
                sig in server_val
                for sig in sigs.get("server", [])
            )

            if header_hit or body_hit or server_hit:
                if waf_name not in detected:
                    detected.append(waf_name)

    except Exception:
        pass

    # ── WAF trigger request ────────────────────────────────────────────────
    # send a request with a classic XSS payload
    # WAFs often block this and show their block page
    # which reveals them even if normal requests don't
    try:
        trigger_url = f"{base_url}/?id=1<script>alert(1)</script>&q=../etc/passwd"
        r2 = requests.get(
            trigger_url,
            timeout=config.get("timeout", 8),
            verify=False,
            headers={"User-Agent": "Mozilla/5.0 Chrome/122.0.0.0"},
            allow_redirects=False,
        )

        # 403/406/429/503 on a malicious URL = WAF blocking
        if r2.status_code in [403, 406, 429, 503]:
            block_body = r2.text[:1000].lower()
            for waf_name, sigs in WAF_SIGNATURES.items():
                if any(sig in block_body for sig in sigs["body"]):
                    if waf_name not in detected:
                        detected.append(waf_name)

    except Exception:
        pass

    # build bypass headers based on what WAF was detected
    bypass_headers = dict(WAF_BYPASS_HEADERS)  # always use IP spoofing
    if "Cloudflare" in detected:
        bypass_headers["CF-Connecting-IP"] = "127.0.0.1"
    if "Akamai" in detected:
        bypass_headers["True-Client-IP"] = "127.0.0.1"

    return {
        "waf":             detected,
        "waf_detected":    len(detected) > 0,
        "bypass_headers":  bypass_headers,
    }


# ══════════════════════════════════════════════════════
#  SECTION 2 — TECH FINGERPRINTING
# ══════════════════════════════════════════════════════

def run_tech_fingerprint(base_url, config):
    """
    Fetches the target and detects technology stack from:
    - Response headers
    - HTML body content
    - Cookie names
    - Meta tags
    - Script/link tag patterns

    Returns list of detected technologies.
    """

    detected = []

    try:
        r = requests.get(
            base_url,
            timeout=config.get("timeout", 8),
            verify=False,
            headers={"User-Agent": "Mozilla/5.0 Chrome/122.0.0.0 Safari/537.36"},
            allow_redirects=True,
        )

        body_lower    = r.text[:5000].lower()
        headers_lower = {
            k.lower(): v.lower()
            for k, v in r.headers.items()
        }
        # cookie names
        cookie_str = " ".join(r.cookies.keys()).lower()

        # meta generator tag — many CMS put their name here
        # e.g. <meta name="generator" content="WordPress 6.4">
        meta_gen = ""
        meta_match = re.search(
            r'<meta[^>]+name=["\']generator["\'][^>]+content=["\']([^"\']+)["\']',
            r.text, re.I
        )
        if meta_match:
            meta_gen = meta_match.group(1).lower()

        for tech_name, sigs in TECH_SIGNATURES.items():

            hit = False

            # check body
            if any(sig.lower() in body_lower for sig in sigs["body"]):
                hit = True

            # check header names
            if any(h.lower() in headers_lower for h in sigs["headers"]):
                # also check header values if specified
                if "header_values" in sigs:
                    for hv in sigs["header_values"]:
                        for hk, hval in headers_lower.items():
                            if hv in hval:
                                hit = True
                else:
                    hit = True

            # check cookies
            if any(c.lower() in cookie_str for c in sigs["cookies"]):
                hit = True

            # check meta generator
            if any(m.lower() in meta_gen for m in sigs["meta"]):
                hit = True

            if hit and tech_name not in detected:
                detected.append(tech_name)

    except Exception:
        pass

    return detected


