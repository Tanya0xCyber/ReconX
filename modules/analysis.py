def run_analysis(results, config):
    """
    Main entry point — reconx.py calls this as:
        run_analysis(results, config)

    Unlike other modules, this one receives the FULL
    results dict from all previous stages.

    It adds its own findings back into the same dict
    and returns the updated version.
    """

    analysis_results = {}

    # get base URL from validation results
    base_url = results.get("base_url", "")
    if not base_url:
        domain   = results.get("domain", "")
        base_url = f"https://{domain}" if domain else ""

    # ── 1. WAF detection ──────────────────────────────────────────────────
    if base_url:
        if config.get("verbose"):
            print("    running WAF detection...")

        waf_results = run_waf_detection(base_url, config)
        analysis_results["waf"]            = waf_results.get("waf", [])
        analysis_results["waf_detected"]   = waf_results.get("waf_detected", False)
        analysis_results["bypass_headers"] = waf_results.get("bypass_headers", {})

    # ── 2. tech fingerprinting ────────────────────────────────────────────
    if base_url:
        if config.get("verbose"):
            print("    running tech fingerprinting...")

        tech = run_tech_fingerprint(base_url, config)
        analysis_results["tech_stack"] = tech

        # merge into main results so vuln hints can see it
        results["tech_stack"] = tech

    # ── 3. vulnerability hints ────────────────────────────────────────────
    if config.get("verbose"):
        print("    running vulnerability hints...")

    # merge analysis results into main results
    # so hint rules can access everything
    results.update(analysis_results)

    hints = run_vuln_hints(results)
    analysis_results["vuln_hints"] = hints

    # count by severity for summary
    analysis_results["hint_summary"] = {
        "critical": sum(1 for h in hints if h["severity"] == "Critical"),
        "high":     sum(1 for h in hints if h["severity"] == "High"),
        "medium":   sum(1 for h in hints if h["severity"] == "Medium"),
        "low":      sum(1 for h in hints if h["severity"] == "Low"),
        "info":     sum(1 for h in hints if h["severity"] == "Info"),
        "total":    len(hints),
    }

    # ── 4. chain analysis ─────────────────────────────────────────────────
    if config.get("verbose"):
        print("    running chain analysis...")

    chains = run_chain_analysis(results, hints)
    analysis_results["chains"] = chains

    return analysis_results