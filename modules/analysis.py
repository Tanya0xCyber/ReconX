def run_chain_analysis(results, hints):
    """
    Looks at ALL findings together and identifies
    attack chains — combinations of vulnerabilities
    that are more dangerous together than separately.

    Think of it like: finding two puzzle pieces that fit.

    Example chain:
    JS file has an API key  +  /api/v1/users endpoint found
    → attacker uses the key to authenticate to the API
    → dumps all user data
    → account takeover

    Returns list of chain descriptions.
    """

    chains = []

    # helper: check if a hint ID was triggered
    triggered_ids = {h["id"] for h in hints}

    def has(hint_id):
        return hint_id in triggered_ids

    def has_tech(tech):
        return tech in results.get("tech_stack", [])

    # ── chain 1: JS secrets + API endpoints = ATO ─────────────────────────
    if has("JS-002") and results.get("js_endpoints"):
        chains.append({
            "title":    "JS Secret + API Endpoint → Account Takeover",
            "severity": "Critical",
            "steps": [
                "1. Leaked API key/token found in JS file",
                "2. API endpoints discovered from JS analysis",
                "3. Use the key to authenticate against API",
                "4. Enumerate users, read private data, or escalate privileges",
            ],
            "components": ["JS Secrets", "API Endpoints"],
        })

    # ── chain 2: subdomain takeover + cookies ─────────────────────────────
    if has("SUB-001"):
        chains.append({
            "title":    "Subdomain Takeover → Session Hijack",
            "severity": "Critical",
            "steps": [
                "1. Subdomain points to unclaimed external service",
                "2. Register the external service (free on most platforms)",
                "3. Host malicious page on the subdomain",
                "4. If cookies are not scoped properly → steal session cookies",
                "5. Use stolen session to access victim account",
            ],
            "components": ["Subdomain Takeover", "Cookie Scope"],
        })

    # ── chain 3: no SPF + no DMARC = email spoofing ───────────────────────
    if has("DNS-002") and has("DNS-003"):
        chains.append({
            "title":    "Missing SPF + Missing DMARC → Email Spoofing",
            "severity": "High",
            "steps": [
                "1. No SPF record → any server can send as this domain",
                "2. No DMARC → receiving servers won't reject spoofed mail",
                "3. Send phishing email appearing to come from target domain",
                "4. Employees/customers receive convincing phishing email",
            ],
            "components": ["Missing SPF", "Missing DMARC"],
        })

    # ── chain 4: exposed DB + no auth ─────────────────────────────────────
    db_ports = [6379, 27017, 9200, 5984, 11211]
    open_db_ports = [
        p for p in results.get("open_ports", [])
        if p.get("port") in db_ports
    ]
    if open_db_ports:
        db_names = [SERVICE_NAMES_SHORT.get(p["port"], str(p["port"]))
                    for p in open_db_ports]
        chains.append({
            "title":    f"Exposed Database → Direct Data Access",
            "severity": "Critical",
            "steps": [
                f"1. Database port(s) open: {', '.join(db_names)}",
                "2. Connect directly with default/no credentials",
                "3. Read/exfiltrate entire database",
                "4. Look for user credentials → reuse on login portals",
            ],
            "components": ["Open Database Port", "No Authentication"],
        })

    # ── chain 5: WordPress + exposed admin ────────────────────────────────
    if has_tech("WordPress") and has("ADM-001"):
        chains.append({
            "title":    "WordPress + Exposed Admin → Full Site Takeover",
            "severity": "High",
            "steps": [
                "1. WordPress installation detected",
                "2. wp-admin panel accessible publicly",
                "3. Brute-force or credential stuff wp-login.php",
                "4. Install malicious plugin → RCE on the server",
            ],
            "components": ["WordPress", "Exposed Admin Panel"],
        })

    # ── chain 6: Docker API exposed ───────────────────────────────────────
    if has("PORT-001"):
        chains.append({
            "title":    "Exposed Docker API → Full Server Takeover",
            "severity": "Critical",
            "steps": [
                "1. Docker API on port 2375 with no TLS/auth",
                "2. List all containers: GET /containers/json",
                "3. Create new container mounting host filesystem at /host",
                "4. Write SSH key to /host/root/.ssh/authorized_keys",
                "5. SSH into the actual host as root",
            ],
            "components": ["Docker API", "No Authentication"],
        })

    # ── chain 7: Kubernetes exposed ───────────────────────────────────────
    if has("PORT-005"):
        chains.append({
            "title":    "Exposed Kubernetes API → Cluster Takeover",
            "severity": "Critical",
            "steps": [
                "1. Kubernetes API server (6443) publicly accessible",
                "2. Check for anonymous access: kubectl --insecure get pods",
                "3. Enumerate secrets: kubectl get secrets -A",
                "4. Exec into containers: kubectl exec -it pod -- /bin/bash",
                "5. Access cloud metadata for IAM credentials",
            ],
            "components": ["Kubernetes API", "Possible Anonymous Access"],
        })

    return chains


# short service names for chain descriptions
SERVICE_NAMES_SHORT = {
    6379:  "Redis",
    27017: "MongoDB",
    9200:  "Elasticsearch",
    5984:  "CouchDB",
    11211: "Memcached",
}


