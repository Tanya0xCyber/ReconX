def run_vuln_hints(results):
    """
    Runs all vuln hint rules against the collected results.

    Each rule has a "check" function (lambda) that takes
    the full results dict and returns True/False.

    If True → the condition for that vulnerability is met
    → we add it to the hints list.

    These are HINTS not confirmed vulnerabilities.
    They tell the pentester "go look here manually."
    """

    hints = []

    for rule in VULN_HINT_RULES:
        try:
            # run the check lambda against all results
            triggered = rule["check"](results)

            if triggered:
                hints.append({
                    "id":       rule["id"],
                    "title":    rule["title"],
                    "severity": rule["severity"],
                    "detail":   rule["detail"],
                })

        except Exception:
            # if a rule crashes (missing key etc.) just skip it
            pass

    # sort by severity: Critical first
    severity_order = {
        "Critical": 0,
        "High":     1,
        "Medium":   2,
        "Low":      3,
        "Info":     4,
    }
    hints.sort(key=lambda x: severity_order.get(x["severity"], 5))

    return hints


